#include "libc-hls-synthesizable.h"
int hls_abs(int n) {
   
   return abs(n);
}

double hls_acos(double x) {
   
   return acos(x);
}

double hls_asin(double x) {
   
   return asin(x);
}

double hls_atan(double x) {
   
   return atan(x);
}

double hls_atan2(double y, double x) {
   
   return atan2(y, x);
}
